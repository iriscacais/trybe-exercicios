let status = 'reprovada';

switch (status) {

    case 'aprovada':
        console.log ('Parabéns, você foi aprovado')
break;
    case 'lista':
        console.log ('Você está em nossa lista de espera')
break;
    case 'reprovada':
        console.log ('Você foi reprovado')
break;
    default:
        console.log ('Informação incorreta')
}